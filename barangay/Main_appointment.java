    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
          // Make sure a row is selected in the table
    int selectedRow = tbl1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an appointment to cancel.");
        return;
    }

    // Get data from the selected row
    String date = tbl1.getValueAt(selectedRow, 0).toString();
    String time = tbl1.getValueAt(selectedRow, 1).toString();
    String doctor = tbl1.getValueAt(selectedRow, 2).toString();

    // Confirm cancellation
    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to cancel this appointment?\n"
        + "Date: " + date + "\nTime: " + time + "\nDoctor: " + doctor,
        "Cancel Appointment",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        // Connect to MySQL
        String url = "jdbc:mysql://localhost:3306/appointment_system";
        String dbUser = "root";
        String dbPass = "";

        Connection con = DriverManager.getConnection(url, dbUser, dbPass);

        // Delete query
        String query = "DELETE FROM date WHERE Date = ? AND Time = ? AND Drname = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, date);
        pst.setString(2, time);
        pst.setString(3, doctor);

        int rows = pst.executeUpdate();

        if (rows > 0) {
            // Remove row from table
            DefaultTableModel model = (DefaultTableModel) tbl1.getModel();
            model.removeRow(selectedRow);

            JOptionPane.showMessageDialog(this, "Appointment canceled successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "No matching appointment found in the database.");
        }

        pst.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error canceling appointment: " + e.getMessage());
        e.printStackTrace();
    }

    }//GEN-LAST:event_jButton14ActionPerformed

