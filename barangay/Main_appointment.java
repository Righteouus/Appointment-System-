    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
   // Replace these names with your actual component names
java.util.Date date1 = date.getDate();  // e.g., com.toedter.calendar.JDateChooser
String time1 = (String) time.getSelectedItem();
String doctor1 = (String) doctorComboBox.getSelectedItem();

// Validation
if (date1 == null || time1 == null || doctor1 == null || time1.isEmpty() || doctor1.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please complete all fields before submitting.");
    return;
}

try {
    // Convert date to SQL format
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String sqlDate = sdf.format(date1); //  fixed

    //  Get logged-in patient ID from session
    int patientId = UserSession.getUserId();

    // Connect to MySQL
    String url = "jdbc:mysql://localhost:3306/appointment";
    String dbUser = "root";
    String dbPass = "";  // change if you have a password

    Connection con = DriverManager.getConnection(url, dbUser, dbPass);

    //  Include Patient_ID in the INSERT query
    String query = "INSERT INTO appointment_date (Patient_ID, Date, Time, Drname) VALUES (?, ?, ?, ?)";
    PreparedStatement pst = con.prepareStatement(query);
    pst.setInt(1, patientId);
    pst.setString(2, sqlDate);
    pst.setString(3, time1);
    pst.setString(4, doctor1);

    int rows = pst.executeUpdate();

    if (rows > 0) {
        JOptionPane.showMessageDialog(this, "Appointment saved successfully!");

        //  Add the new record to JTable (tbl1)
        DefaultTableModel model = (DefaultTableModel) tbl1.getModel();
        model.addRow(new Object[]{sqlDate, time1, doctor1});

        
    } else {
        JOptionPane.showMessageDialog(this, "Failed to save appointment.");
    }

    pst.close();
    con.close();

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    e.printStackTrace();
}

    }//GEN-LAST:event_jButton13ActionPerformed

